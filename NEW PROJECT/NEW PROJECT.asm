#INCLUDE "P16F887.INC"

	__CONFIG _CONFIG1, (_CP_OFF & _WDT_OFF & _PWRTE_ON & _XT_OSC & _LVP_OFF)
	__CONFIG _CONFIG2, _BOR21V

reg1 EQU 0x20

ORG 0X00

BANKSEL TRISB
BCF TRISB, 0
BANKSEL PORTB

Inicio
	BSF PORTB,0
	CALL TIEMPO
	BCF PORTB,0
	CALL TIEMPO
	GOTO Inicio

TIEMPO
	MOVLW .5
	MOVWF reg1
SIGO
	DECFSZ reg1,1 
	GOTO SIGO
	Return

END